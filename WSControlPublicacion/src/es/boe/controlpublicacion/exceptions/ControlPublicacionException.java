package es.boe.controlpublicacion.exceptions;

public class ControlPublicacionException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1928336819153823830L;

	public ControlPublicacionException() {
		super();
	}

	public ControlPublicacionException(String message, Throwable cause) {
		super(message, cause);
	}

	public ControlPublicacionException(String message) {
		super(message);
	}

	public ControlPublicacionException(Throwable cause) {
		super(cause);
	}

}
